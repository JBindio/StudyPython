# 데이터 타입

# 숫자형 (정수(int), 실수(float))
# 2진수(bin, b)
a = 0b101011
f = 0b11111111
print(a)
print(f)
print(bin(10))

# 8진수(oct, o)
print(0o52)
print(oct(10))

# 16진수(hex, x)
print(0xff)

# 숫자 단위가 클때 _ 활용
salery = 4_500_000
print(salery)

# 타입 확인하기
print(type(1))
print(type(3.14))

z = False
print(type(z))

z = 10
print(type(z))